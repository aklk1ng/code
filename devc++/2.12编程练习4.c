 {
 	printf("For he's a jolly good fellow!\n");
 }
 void deny(void) //定义deny函数 
 {
 	printf("Which nobody can deny!\n");
 }
 int main()
{
	jolly(); //调用（）函数 
	jolly(); //调用（）函数 
	jolly(); //调用（）函数 
	deny(); // 调用（）函数 
	
	getchar();
	return 0;
	
	
	
 } 
 
 
