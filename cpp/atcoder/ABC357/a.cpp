using ll = long long;
const int mod = 998244353;

void solve() {
    int n, m;
    std::cin >> n >> m;

    int ans = 0;
    for (int i = 0; i < n; i++) {
        int h;
        std::cin >> h;
        m -= h;
        if (m >= 0) {
            ans++;
        }
    }
    std::cout << ans << '\n';
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);
    std::cout.tie(nullptr);
    int T = 1;
    if (false) {
        std::cin >> T;
    }
    while (T--) {
        solve();
    }

    return 0;
}
